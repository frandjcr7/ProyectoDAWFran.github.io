<section>

    <h2>Historia de la Informática</h2>
    
    <p>La informática ha evolucionado enormemente desde sus inicios, pasando de simples máquinas mecánicas a los sistemas avanzados que usamos hoy en día. A continuación, repasaremos los hitos más destacados en la historia de esta disciplina.</p>
    
    <h3>Orígenes</h3>
    <p>La historia de la informática comienza en el siglo XIX con Charles Babbage, quien diseñó la Máquina Analítica, considerada el precursor de los ordenadores modernos. Ada Lovelace, una matemática brillante, es reconocida como la primera programadora por desarrollar algoritmos para esta máquina.</p>

    <h3>Primera Generación (1940-1956)</h3>
    <p>Las primeras computadoras electrónicas, como la ENIAC, utilizaban tubos de vacío para procesar datos. Estas máquinas eran enormes, costosas y consumían gran cantidad de energía.</p>
    
    <h3>Segunda Generación (1956-1963)</h3>
    <p>Con la invención del transistor, las computadoras se volvieron más pequeñas, rápidas y fiables. Además, se introdujeron lenguajes de programación como COBOL y FORTRAN, que facilitaron la creación de software.</p>

    <h3>Tercera Generación (1964-1971)</h3>
    <p>La aparición de los circuitos integrados marcó esta etapa. Los ordenadores se hicieron aún más compactos y accesibles, sentando las bases para los sistemas modernos.</p>

    <h3>Revolución del PC (Años 80)</h3>
    <p>Durante esta época, los ordenadores personales (PC) se popularizaron. Empresas como Apple y Microsoft lideraron este cambio, introduciendo productos como el Macintosh y el sistema operativo Windows.</p>

    <h3>Era de Internet (Años 90)</h3>
    <p>La creación de la World Wide Web transformó la informática. La conectividad global permitió el acceso a información y servicios en tiempo real, revolucionando industrias completas.</p>

    <h3>Siglo XXI: Computación Avanzada</h3>
    <p>En la actualidad, la informática abarca áreas como inteligencia artificial, blockchain, realidad virtual y computación en la nube. Estos avances continúan moldeando nuestra sociedad y economía.</p>

